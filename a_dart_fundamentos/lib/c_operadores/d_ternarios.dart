void main() {
  // Operador Ternário - Um IF/ELSE simplificado

  final age = 12;

  //(condição) ? faça algo : faça outra coisa
  // a expressão acima significa se uma codição for true faça algo se não faça outra coisa

  final result = age >= 16 ?  true : false;
  print('É apto a votar? $result');
}
