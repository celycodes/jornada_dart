void main() {
  // Operadores relacionais
  // == igualdade, != diferença, > maior que, < menor que, >= maior igual, <= menor igual

  final age = 18;
  // Regra para tirar habilitação

  if (age >= 18) {
    print('Pode tirar carteira de motorista');
  } else if (age < 18) {
    print('Não pode tirar carteira de motorista');
  }
}
